      <h1 class="font-bold underline underline-offset-[18px] decoration-black decoration-[10px] py-12">SPAX</h1>
      <p class="spax-about text-[20px] text-justify">
        SPAX is an annual student professional awareness event organized by
        IEEE at UCLA. It focuses on providing students with professional
        awareness, networking opportunities, and industry contact to not
        only prepare participants to be leaders in their future careers, but
        also provide opportunities to those who may not have access. Every
        year, the X switches between C for conference and V for venture.
        During SPAC, a group of selected speakers come to share their
        experiences with participants. During SPAV, participants venture out
        to selected companies to learn more about their prospects.
      </p>
